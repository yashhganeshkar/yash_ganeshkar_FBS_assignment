
# Accept age and amount of ticket for 5 men and calculate the bus ticket for particular age

#age 1

age1 = int(input("Enter Age 1 = "))
amount1 = float(input("Enter Amount 1 = "))
if(age1 <= 12):
    finalAmount = amount1 * 0.70
    print("Amount Payable ",finalAmount)
elif(age1 > 59):
    finalAmount = amount1 * 0.50
    print("Amount Payable ",finalAmount)
else:
    finalAmount = amount1
    print("Amount Payable ",finalAmount)

